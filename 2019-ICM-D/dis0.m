function [d]=dis0(A,B) %同一层两个点的距离
m=(ceil(A(1,1)/684)-1)*684+370;
n=1018;
f=6-ceil(A(1,1)/684);
if f==1
    d=abs(A(1,1)-B(1,1))+abs(A(1,2)-B(1,2)); %地下二楼直接算
else
    if A(1,2)<n && B(1,2)<n  && (A(1,1)-m)*(B(1,1)-m)<0  %在图的分界线右侧,且分割上下
        d=abs(A(1,1)-B(1,1))+2*n-A(1,2)-B(1,2);
    else
         d=abs(A(1,1)-B(1,1))+abs(A(1,2)-B(1,2));
    end
end
    