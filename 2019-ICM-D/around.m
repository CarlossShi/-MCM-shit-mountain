function [R]=around(A,b) %A为1×2矩阵,参数为0时返回周围8格位置的9×2矩阵
                       %参数为1-9时，返回1×2的矩阵代表前进方向或者原地不动
                       %参数为-9--1时，反向移动
if b<0
    b=10+b;
end  %现在b非负了
if b==0
    R=[A(1,1)-1,A(1,2)-1;A(1,1)-1,A(1,2)  ;A(1,1)-1,A(1,2)+1;...
       A(1,1)  ,A(1,2)-1;A(1,1)  ,A(1,2)  ;A(1,1)  ,A(1,2)+1;...
       A(1,1)+1,A(1,2)-1;A(1,1)+1,A(1,2)  ;A(1,1)+1,A(1,2)+1];
else
    R=around(A,0);
    R=R(b,:);
end
